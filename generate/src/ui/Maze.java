    private void jPanel2KeyPressed(java.awt.event.KeyEvent evt) throws RemoteException, MalformedURLException, NotBoundException {
        // On récupère la position de la Pièce courante
    	int x = currentPiece.getPosX();
        int y = currentPiece.getPosY();
        int exX = x;
        int exY = y;
    	
    	// On récupère les portes de la Piece courante
        ArrayList<Porte> listPortes = currentPiece.getListePortes();
        ArrayList<String> listPosition = new ArrayList<String>();
        for (Porte porte : listPortes) {
        	listPosition.add(porte.getPositionPorte());
        }
        
        // Touche clavier
        int keyCode = evt.getKeyCode();
        switch(keyCode) {
        case KeyEvent.VK_UP: // Flèche Haut
        	if (listPosition.contains("NORD")) {
        		// Change de pièce : On monte
        		x = x-1;
        	} else {
        		System.out.println("Pas de porte au Nord.");
        		Util.afficherPopUp("Pas de porte au Nord.");
        	}
        	break;
        case KeyEvent.VK_DOWN: // Flèche Bas
        	if (listPosition.contains("SUD")) {
        		// Change de pièce : On descend
        		x = x+1;
        	} else {
        		System.out.println("Pas de porte au Sud.");
        		Util.afficherPopUp("Pas de porte au Sud.");
        	}
        	break;
        case KeyEvent.VK_LEFT: // Flèche Gauche 
        	if (listPosition.contains("OUEST")) {
        		// Change de pièce : A gauche
        		y = y-1;
        	} else {
        		System.out.println("Pas de porte à l'Ouest.");
        		Util.afficherPopUp("Pas de porte à l'Ouest.");
        	}
        	break;
        case KeyEvent.VK_RIGHT: // Flèche Droite
        	if (listPosition.contains("EST")) {
        		// Change de pièce : A droite
        		y = y+1;
        	} else {
        		System.out.println("Pas de porte à l'Est.");
        		Util.afficherPopUp("Pas de porte à l'Est.");
        	}
        	break;
        }
        
        // Si on change de Piece
        if (currentPiece.getPosX() != x || currentPiece.getPosY() != y) {
        	// Si récupère le serveur
        	if(currentPiece.getNomServer().equals("alpha")) {
    		 	laby = (Labyrinthe) Naming.lookup("MonServeur1");
        	} else if (currentPiece.getNomServer().equals("beta")) {
             	laby = (Labyrinthe) Naming.lookup("MonServeur2"); 
            }
        	
            // On retire le perso de la salle
            listeLabels.get("jLabel"+exX+exY).setText(null);
        	
    		// On récupère toutes les Pieces
        	ArrayList<Piece> listPiece = laby.getPiece();
        	for (Piece piece : listPiece) {
        		// Si même X et Y -> on récupère la nouvelle Piece
        		if (piece.getPosX() == x && piece.getPosY() == y) {
        			currentPerso.setIdPiece(piece.getIdPiece());
        			currentPiece = piece;
        		}
        	}
        	
        	afficherCase(currentPiece);
        	
        	// On affiche le combat
        	ArrayList<String> listP = new ArrayList<String>(laby.getPersonnagesSamePiece(currentPerso.getNomIndiv()));
        	JComboBox jcb = new JComboBox(listP.toArray());
        	Combat fenCombat = null;
        	Combat fenCombatJ2 = null;
        	if(listP.size() > 1) {
        		try {
					UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
					UIManager.put("OptionPane.background", Color.WHITE);
	                UIManager.put("Panel.background", Color.WHITE);
	                UIManager.put("OptionPane.messageForeground", Color.BLACK);
	        		JOptionPane.showMessageDialog(null, jcb, "Choix d'un ennemi", JOptionPane.QUESTION_MESSAGE);
	        		fenCombat = new Combat(currentPerso, jcb.getSelectedItem().toString());
				} catch (ClassNotFoundException | InstantiationException | IllegalAccessException | UnsupportedLookAndFeelException e) {
					e.printStackTrace();
				}
        	} else {
        		fenCombat = new Combat(currentPerso);
        	}
        	fenCombat.setVisible(true);
        }
    }
    
    public void afficherCase(Piece p) {
    	// On récupère la position de la Pièce courante
    	int x = p.getPosX();
        int y = p.getPosY();
        
    	//fond blanc pour la pièce courante
    	listePanels.get("jPanel"+x+y).setBackground(Color.white);
    	
    	// Dessin des portes
        ArrayList<Porte> listPortes = p.getListePortes();
        int a=5, b=5, c=5, d=5;
        for (Porte porte : listPortes) {
        	switch (porte.getPositionPorte()) {
        		case "NORD" : // Haut
        			a = 1;
        			break;
        		case "EST" : // Droite
        			d = 1;
        			break;
        		case "SUD" : // Bas
        			c = 1;
        			break;
        		case "OUEST" : // Gauche
        			b = 1;
        			break;
        	}
        }
        
        // Dessin du perso
        listeLabels.get("jLabel"+x+y).setText("X");
        
        listePanels.get("jPanel"+x+y).setBorder(BorderFactory.createMatteBorder(a, b, c, d, Color.BLACK));
        listePanels.get("jPanel"+x+y).revalidate();
        listePanels.get("jPanel"+x+y).repaint();
    }
    
